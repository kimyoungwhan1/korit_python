#산술 연산자
num1 = 30
num2 = 12
print(f"num1 + num2 = {num1 + num2}") #덧셈
print(f"num1 - num2 = {num1 - num2}")
print(f"num1 * num2 = {num1 * num2}")
print(f"num1 / num2 = {num1 / num2}") #실수 몫을 반환을 해준다.
print(f"num1 // num2 = {num1 // num2}") #정수 몫을 반환해 준다.
print(f"num1 % num2 = {num1 % num2}") #나머지를 반환해 준다.

#대입 연산자
